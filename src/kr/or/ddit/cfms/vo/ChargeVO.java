package kr.or.ddit.cfms.vo;

public class ChargeVO {
	private int cha_no;
	private String cus_id;
	private String cha_dt;
	private int cha_pr;
	
	
	
	public int getCha_no() {
		return cha_no;
	}
	public void setCha_no(int cha_no) {
		this.cha_no = cha_no;
	}
	public String getCus_id() {
		return cus_id;
	}
	public void setCus_id(String cus_id) {
		this.cus_id = cus_id;
	}
	public String getCha_dt() {
		return cha_dt;
	}
	public void setCha_dt(String cha_dt) {
		this.cha_dt = cha_dt;
	}
	public int getCha_pr() {
		return cha_pr;
	}
	public void setCha_pr(int cha_pr) {
		this.cha_pr = cha_pr;
	}
	
	
}
